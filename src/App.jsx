const calculateBalanceProgress = () => {
	let grandTotal = 0;
	let grandPaid = 0;

	data_balance.forEach((person) => {
		grandTotal += person.total;

		if (person.paid && Array.isArray(person.paid)) {
			person.paid.forEach((p) => {
				grandPaid += p.paid;
			});
		}
	});

	return {
		grandTotal,
		grandPaid,
		percent: grandTotal > 0 ? Math.round((grandPaid / grandTotal) * 100) : 0,
	};
};

